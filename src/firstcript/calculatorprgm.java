package firstcript;

public class calculatorprgm {

	public static void main(String[] args) {
		int a=2,b=5;
		int ch='*';
		switch(ch)
		{
		case '+':System.out.println(a+b);
		break;
		case '-':System.out.println(a-b);
		break;
		case '*':System.out.println(a*b);
		break;
		case '/':System.out.println(a/b);
		break;
		default:System.out.println("invalid input");
		break;
		}
		
		{
		
		}

	}

}
